/* Set the height of the navigation bar to 24px */
function openNav() {
    document.getElementById("navbar").style.height = "24px";
    document.getElementById("navbar").style.width = "100%";
}

/* Set the height of the navigation to 0 */
function closeNav() {
    document.getElementById("navbar").style.height = "0";
    document.getElementById("navbar").style.width = "0";
}